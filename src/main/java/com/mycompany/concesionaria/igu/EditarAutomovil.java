    private void cargarDatos(int id) {  
        this.auto = control.traerAuto(id);
        
        txtColor.setText(auto.getColor());
        txtMarca.setText(auto.getMarca());
        txtModelo.setText(auto.getModelo());
        txtMotor.setText(auto.getMotor());
        txtPatente.setText(auto.getPatente());

        if(auto.getCantPuerta().equalsIgnoreCase("3")){
            cbPuertas.setSelectedIndex(1);
        }else if(auto.getCantPuerta().equalsIgnoreCase("5")){
            cbPuertas.setSelectedIndex(2);
        }else{
            cbPuertas.setSelectedIndex(0);
        }
    }
